// Generate SQL DDL from the local Prisma schema, suitable for bootstrapping a
// fresh PGlite instance in apps/pos-desktop.
//
// Usage: node scripts/generate-local-sql.mjs
//
// Uses `prisma migrate diff --from-empty --to-schema ... --script` under the
// hood, capturing only the SQL output (not the "Loaded Prisma config" stderr
// line).

import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const OUTPUT = join(ROOT, 'prisma', 'generated', 'local-schema.sql');

// Extend PATH so 'prisma' resolves to the local node_modules/.bin/prisma.cmd
// on Windows or the symlink on Unix.
const binDir = join(ROOT, 'node_modules', '.bin');
const pathSep = process.platform === 'win32' ? ';' : ':';
const env = { ...process.env, PATH: `${binDir}${pathSep}${process.env.PATH ?? ''}` };

console.log('Generating local schema SQL…');

const stdout = execSync(
  `prisma migrate diff --from-empty --to-schema prisma/schema-local --script --config prisma.local.config.ts`,
  {
    cwd: ROOT,
    encoding: 'utf-8',
    env,
    shell: true,
    stdio: ['pipe', 'pipe', 'pipe'],
  },
);

const sql = stdout.trim();

if (!sql) {
  console.error('ERROR: prisma migrate diff produced no output.');
  process.exit(1);
}

// Write raw SQL (for reference / manual inspection)
writeFileSync(join(ROOT, 'prisma', 'generated', 'local-schema.sql'), sql, 'utf-8');

// Write TypeScript module exporting the SQL string — this is what the
// frontend imports at build time, avoiding any need for Vite raw-import
// plugins or runtime filesystem access.
const tsContent = `// Auto-generated by scripts/generate-local-sql.mjs — DO NOT EDIT.
// Run \`pnpm db:generate:local-sql\` to regenerate.

/** DDL for the local (shared-only) Prisma schema, as produced by \`prisma migrate diff --from-empty --to-schema\`. */
export const LOCAL_SCHEMA_SQL = ${JSON.stringify(sql)};
`;
const tsOut = join(ROOT, 'src', 'local-schema.ts');
writeFileSync(tsOut, tsContent, 'utf-8');

console.log(`✓ Schema SQL (${sql.length} bytes) written to prisma/generated/local-schema.sql`);
console.log(`✓ TypeScript module written to src/local-schema.ts`);

